from enum import Enum
from typing import Dict


class TransformType(Enum):
    """四化类型"""
    HUA_LU = "化禄"      # 化禄 - 财禄、享受
    HUA_QUAN = "化权"    # 化权 - 权力、成就
    HUA_KE = "化科"      # 化科 - 科名、名誉
    HUA_JI = "化忌"      # 化忌 - 忌恨、阻碍


class TransformCycleType(Enum):
    """
    成住坏空四周期类型

    佛学宇宙观中的成住坏空四劫，对应紫微斗数四化理论：
    - 成（缘起）→ 化禄：万事万物形成的阶段，代表缘起、开始、福德
    - 住（持续）→ 化权：成熟稳定阶段，代表持续、权力、成就
    - 坏（衰减）→ 化科：开始衰败阶段，代表衰减、科名、名誉受损
    - 空（结束）→ 化忌：归于空无阶段，代表结束、忌恨、阻碍

    参考梁若瑜《飞星紫微斗数》中成住坏空与四化对应理论
    """
    CHENG = "成"     # 缘起 - 化禄
    ZHU = "住"      # 持续 - 化权
    HUAI = "坏"     # 衰减 - 化科
    KONG = "空"     # 结束 - 化忌


class TransformInteraction(Enum):
    """四化交互类型"""
    LU_QUAN_KE_JI = "禄权科忌"  # 完整配置
    LU_QUAN_KE = "禄权科"       # 三奇嘉会
    LU_QUAN = "禄权"            # 权禄组合
    LU_KE = "禄科"              # 禄科组合
    QUAN_KE = "权科"            # 权科组合
    LU_JI = "禄忌"              # 禄忌对冲
    QUAN_JI = "权忌"            # 权忌对冲
    KE_JI = "科忌"              # 科忌对冲


class TransformPathType(Enum):
    """飞化路径类型"""
    # 禄转忌：本宫化禄→忌入某宫
    LU_ZHUAN_JI = "禄转忌"
    # 忌转忌：本宫化忌→忌入某宫，再从该宫化忌
    JI_ZHUAN_JI = "忌转忌"
    # 追禄：忌入某宫后，该宫再化禄（同星曜）
    ZHUI_LU = "追禄"
    # 追权：忌入某宫后，该宫再化权（同星曜）
    ZHUI_QUAN = "追权"
    # 追忌：禄入某宫后，该宫再化忌（非必同星曜）
    ZHUI_JI = "追忌"
    # 二度转忌：忌转忌后再转忌
    JI_ZHUAN_JI_2 = "二度转忌"
    # 三度转忌：忌转忌后再转忌再转忌
    JI_ZHUAN_JI_3 = "三度转忌"


